    private void guestlistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guestlistActionPerformed

        boolean check=true;

        javax.swing.table.DefaultTableModel model;

        guesttable.setModel(new javax.swing.table.DefaultTableModel(null,new String[] {"Std Roll No", "Guest 1 Roll No", "Guest 1 Name","Guest 1 Contact", "Guest 2 Roll No", "Guest 2 Name", "Guest 2 Contact","Entry Date"}));
        try{
            Connection con=new DBConnection().getConnection();
            Statement s=con.createStatement();
            ResultSet rs=s.executeQuery("select * from guest_data");
            while(rs.next())
            {
                check=false;
                String stdrollno=rs.getString(1);
                String g1rollno=rs.getString(2);
                String g1name=rs.getString(3);
                String g1contact=String.valueOf(rs.getLong(4));
                String g2rollno=rs.getString(5);
                String g2name=rs.getString(6);
                String g2contact=String.valueOf(rs.getLong(7));
                String date=String.valueOf(rs.getDate(8));

                String[] data={stdrollno,g1rollno,g1name,g1contact,g2rollno,g2name,g2contact,date};
                model=(javax.swing.table.DefaultTableModel) guesttable.getModel();
                model.addRow(data);
            }

            if(check)
            {
                JOptionPane.showMessageDialog(null, "Table is empty!");
            }

            con.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }

    }//GEN-LAST:event_guestlistActionPerformed

